public class Chapter_2_examples {
	
	/**
	 * NOTES:
	 * everything between curly brackets is part of class: for every opening, there is a closing
	 * public static void is where program starts
	 * methods inside classes
	 * no functions in Java: methods
	 * every statement ends in semicolon
	 * (): where the argument is placed - in methods
	 * String[]: an array, lets us use string methods
	 */

	public static void main(String[] args) {
		// displays text
		System.out.println("Welcome to Java");
		System.out.println("Welcome to the jungle.");
		System.out.println(4-2);
		System.out.println("");
		
		// integer variables?
		int jungle = 2;
		System.out.println("Jungle is " + jungle + ".");
		System.out.println(jungle - 1);
	}

}



